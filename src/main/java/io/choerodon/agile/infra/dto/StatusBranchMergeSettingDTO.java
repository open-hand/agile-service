package io.choerodon.agile.infra.dto;

import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

import io.choerodon.mybatis.annotation.ModifyAudit;
import io.choerodon.mybatis.annotation.VersionAudit;
import io.choerodon.mybatis.domain.AuditDomain;

/**
 * @author superlee
 * @since 2021-04-19
 */
@ModifyAudit
@VersionAudit
@Table(name = "fd_status_branch_merge_setting")
public class StatusBranchMergeSettingDTO extends AuditDomain {

    @Id
    @GeneratedValue
    private Long id;

    private Long issueTypeId;

    private Long statusId;

    private Boolean autoTransform;

    private Long projectId;

    private Long organizationId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getIssueTypeId() {
        return issueTypeId;
    }

    public void setIssueTypeId(Long issueTypeId) {
        this.issueTypeId = issueTypeId;
    }

    public Long getStatusId() {
        return statusId;
    }

    public void setStatusId(Long statusId) {
        this.statusId = statusId;
    }

    public Boolean getAutoTransform() {
        return autoTransform;
    }

    public void setAutoTransform(Boolean autoTransform) {
        this.autoTransform = autoTransform;
    }

    public Long getProjectId() {
        return projectId;
    }

    public StatusBranchMergeSettingDTO setProjectId(Long projectId) {
        this.projectId = projectId;
        return this;
    }

    public Long getOrganizationId() {
        return organizationId;
    }

    public void setOrganizationId(Long organizationId) {
        this.organizationId = organizationId;
    }
}
